    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
        boolean sukses = false;
        try {
            double uang = Double.parseDouble(txtTBayar.getText());
            double bayar = Double.parseDouble(txtPembayaran.getText());
            double kembalian = bayar - uang;
            txtKembalian.setText(Double.toString(kembalian));
            sukses = true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Input harus berupa angka", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtPembayaran.setText("");
            txtPembayaran.requestFocus();
        } finally {
            if (sukses) {
                JOptionPane.showMessageDialog(this, "Pemesanan dan pembayaran tiket telah selesai");
            }

        }
    }//GEN-LAST:event_btnBayarActionPerformed

