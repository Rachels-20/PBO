    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        if (!rbEkonomi.isSelected() && !rbExecutive.isSelected() && !rbSe.isSelected()) {
            JOptionPane.showMessageDialog(this, "Tujuan belum dipilih!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                int nomor = (int) cbTujuan.getSelectedIndex();
                switch (nomor) {
                    case 1: {
                        txtTujuan.setText("Bukittinggi");
                    }

                    break;
                    case 2: {
                        txtTujuan.setText("Solok");
                    }

                    break;
                    case 3: {
                        txtTujuan.setText("Batusangkar");
                    }

                    break;
                    case 4: {
                        txtTujuan.setText("Sijunjung");
                    }

                    break;
                    case 5: {
                        txtTujuan.setText("Padang Panjang");
                    }

                    break;
                }

                String tujuan = cbTujuan.getSelectedItem().toString();
                String kelas = "";

                if (rbEkonomi.isSelected()) {
                    kelas = "Ekonomi";
                    txtKelas.setText("Ekonomi");
                } else if (rbExecutive.isSelected()) {
                    kelas = "Executive";
                    txtKelas.setText("Executive");
                } else if (rbSe.isSelected()) {
                    kelas = "Super Executive";
                    txtKelas.setText("Super Executive");
                }

                double harga = 0;
                if (tujuan == "Bukittinggi") {
                    if (kelas == "Ekonomi") {
                        harga = 30000;
                    } else if (kelas == "Executive") {
                        harga = 40000;
                    } else if (kelas == "Super Executive") {
                        harga = 50000;
                    }
                } else if (tujuan == "Batusangkar") {
                    if (kelas == "Ekonomi") {
                        harga = 35000;
                    } else if (kelas == "Executive") {
                        harga = 45000;
                    } else if (kelas == "Super Executive") {
                        harga = 55000;
                    }
                } else if (tujuan == "Solok") {
                    if (kelas == "Ekonomi") {
                        harga = 30000;
                    } else if (kelas == "Executive") {
                        harga = 40000;
                    } else if (kelas == "Super Executive") {
                        harga = 50000;
                    }
                } else if (tujuan == "Sijunjung") {
                    if (kelas == "Ekonomi") {
                        harga = 45000;
                    } else if (kelas == "Executive") {
                        harga = 55000;
                    } else if (kelas == "Super Executive") {
                        harga = 65000;
                    }
                } else if (tujuan == "Padang Panjang") {
                    if (kelas == "Ekonomi") {
                        harga = 25000;
                    } else if (kelas == "Executive") {
                        harga = 35000;
                    } else if (kelas == "Super Executive") {
                        harga = 45000;
                    }
                }
                txtHarga.setText(Double.toString(harga));
                int tiket = Integer.parseInt(txtTiket.getText());
                double total = harga * tiket;
                double pajak = 0.1 * total;
                double totBayar = total + pajak;
                txtTotal.setText(Double.toString(total));
                txtPajak.setText(Double.toString(pajak));
                txtTBayar.setText(Double.toString(totBayar));

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Input harus berupa angka", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtTiket.setText("");
                txtTiket.requestFocus();
                return;
            }

        }

    }//GEN-LAST:event_btnProsesActionPerformed


